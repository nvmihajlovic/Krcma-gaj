function translatePage(lang) {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.innerHTML = translations[lang][key];
        }
    });
    
    // Handle typing text with data-i18n-text attribute
    const typingElement = document.querySelector('.typing-text[data-i18n-text]');
    if (typingElement) {
        const textKey = typingElement.getAttribute('data-i18n-text');
        if (translations[lang] && translations[lang][textKey]) {
            // Complete stop of all typing
            if (window.typingInterval) {
                clearTimeout(window.typingInterval);
                window.typingInterval = null;
            }
            window.isTyping = false;
            window.currentTypingElement = null;
            
            // Hide and completely clear element
            typingElement.style.opacity = '0';
            typingElement.innerHTML = '';
            typingElement.textContent = '';
            typingElement.style.borderRight = 'none';
            
            // Wait for element to be completely cleared
            setTimeout(() => {
                // Update data-text attribute with new translation
                typingElement.setAttribute('data-text', translations[lang][textKey]);
                
                // Show element and start typing
                typingElement.style.opacity = '1';
                if (typeof window.startTypingEffect === 'function') {
                    window.startTypingEffect(typingElement, 500);
                }
            }, 500);
        }
    }
    
    // Handle tooltip translations
    document.querySelectorAll('[data-i18n-tooltip]').forEach(element => {
        const key = element.getAttribute('data-i18n-tooltip');
        if (translations[lang] && translations[lang][key]) {
            element.setAttribute('title', translations[lang][key]);
        }
    });
}
